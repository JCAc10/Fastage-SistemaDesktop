     public void guardarBoleta(){
        try{
            String SQL = "insert into boleta (CodEmple, Cliente, DNI, Fecha, Nom_Produc, Cantidad) values (?,?,?,?,?,?)";
            PreparedStatement pst = con.con.prepareStatement(SQL);
            pst.setString(1, jCod.getText());
            pst.setString(2, jCliente.getText());
            pst.setString(3, jDNI.getText());
            pst.setString(4, jFecha.getText());
            nom=jComboBox1.getSelectedIndex();
            pst.setString(5, jComboBox1.getItemAt(nom));
            pst.setDouble(6, Integer.parseInt(jCantidad.getText()));
            pst.execute();
            JOptionPane.showMessageDialog(null, "Boleta Guardada");
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Revise el c√≥digo del empleado");
        }
    }
     
