    public void mostrarDatos(){
        String[] titulos = {"ID","PRODUCTO", "STOCK", "CATEGORÍA", "PRECIO", "CARACTERISTICAS"};
        String[] registros=new String[6];
        
        DefaultTableModel modelo = new DefaultTableModel(null, titulos);
        String SQL = "SELECT idProduc, Nom_Produc, Stock, c.NomCategoria, PrecioUni, info_prod FROM productos p inner join categoria c on (c.idCategoria=p.idCategoria)";
        try{
            Statement stmt = con.con.createStatement();
            ResultSet rs = stmt.executeQuery(SQL);
            
            while(rs.next()){
                registros[0]= rs.getString("idProduc");
                registros[1]= rs.getString("Nom_Produc");
                registros[2]= rs.getString("Stock");
                registros[3]= rs.getString("NomCategoria");
                registros[4]= rs.getString("PrecioUni");
                registros[5]= rs.getString("info_prod"); 
                modelo.addRow(registros);
            }
            jTable1.setModel(modelo);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error " + e);
        }
    }
    public void actualizarDatos(){
        try{
            String SQL = "update productos set Nom_Produc=?, Stock=?, idCategoria=?, PrecioUni=?, info_prod=? where idProduc=?";
          
            int filaSeleccionada = jTable1.getSelectedRow();
            String dao = (String)jTable1.getValueAt(filaSeleccionada, 0);
            
            PreparedStatement pst = con.con.prepareStatement(SQL);
            pst.setString(1, txtProduc.getText());
            pst.setString(2, txtStock.getText());
            cat=cbCat.getSelectedIndex();
            pst.setString(3, cbCat.getItemAt(cat));
            pst.setDouble(4, Double.parseDouble(txtPrecio.getText()));
            pst.setString(5, txtInfo.getText());
            pst.setString(6, dao);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Actualizado");
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Error " + e);
        } 
        }
    public void eliminarDatos(){
        int filaSeleccionada = jTable1.getSelectedRow();
       try{
           String SQL = "delete from productos where idProduc="+jTable1.getValueAt(filaSeleccionada,0);
           Statement stmt = con.con.createStatement();
           
           int n = stmt.executeUpdate(SQL);
           if(n>0){
               JOptionPane.showMessageDialog(null, "Registro Eliminado");
           }
           
       }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error " + e);
       }
    }
