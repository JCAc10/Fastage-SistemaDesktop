    public void insertarDatos(){
        try{
            String SQL = "insert into productos (Nom_Produc, Stock, idCategoria, PrecioUni, info_prod) values (?,?,?,?,?)";
            PreparedStatement pst = con.con.prepareStatement(SQL);
            pst.setString(1, txtProduc.getText());
            pst.setString(2, txtStock.getText());
            cat=cbCat.getSelectedIndex();
            pst.setString(3, cbCat.getItemAt(cat));
            pst.setDouble(4, Double.parseDouble(txtPrecio.getText()));
            pst.setString(5, txtInfo.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Registro Insertado");
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Error " + e);
        }
    }
